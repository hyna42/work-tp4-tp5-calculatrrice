#include "staticCal.h"

float staticAddition(float a, float b)
{
    return (a + b);
}

float staticSoustraction(float a, float b)
{
    return (a - b);
}

float staticMultiplication(float a, float b)
{
    return (a * b);
}

float staticDivision(float a, float b)
{
    return a / b;
}